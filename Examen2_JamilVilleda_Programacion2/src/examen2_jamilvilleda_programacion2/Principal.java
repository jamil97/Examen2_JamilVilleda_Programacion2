    private void bt_modificarInformacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_modificarInformacionMouseClicked
        try {
            if (jt_informacionPersonalUsuarioComun.getSelectedRow() >= 0) {
                String nombreModificar = JOptionPane.showInputDialog(null, "Ingrese su nombre");
                DefaultTableModel modelo = (DefaultTableModel) jt_informacionPersonalUsuarioComun.getModel();
                String nombre = JOptionPane.showInputDialog(null, "Ingrese el nuevo nombre a agregarse");
                String usuario = JOptionPane.showInputDialog(null, "Ingrese el nuevo nombre de usuario a agregarse");
                String correo = JOptionPane.showInputDialog(null, "Ingrese el nuevo correo de usuario a agregarse");
                String contra = JOptionPane.showInputDialog(null, "Ingrese la nueva contrase単a de usuario");

                modelo.setValueAt(nombre, jt_informacionPersonalUsuarioComun.getSelectedRow(), 0);
                modelo.setValueAt(correo, jt_informacionPersonalUsuarioComun.getSelectedRow(), 1);
                modelo.setValueAt(usuario, jt_informacionPersonalUsuarioComun.getSelectedRow(), 3);
                modelo.setValueAt(contra, jt_informacionPersonalUsuarioComun.getSelectedRow(), 4);

                jt_informacionPersonalUsuarioComun.setModel(modelo);

                adminUsuarioNormal.cargarArchivo();
                for (int i = 0; i < adminUsuarioNormal.getUsuarioNormal().size(); i++) {
                    if (adminUsuarioNormal.getUsuarioNormal().get(i).getNombreCompleto().equals(nombreModificar)) {
                        adminUsuarioNormal.getUsuarioNormal().get(i).setNombreCompleto(nombre);
                        adminUsuarioNormal.getUsuarioNormal().get(i).setCorreoElectronico(correo);
                        adminUsuarioNormal.getUsuarioNormal().get(i).setNombreUsuario(usuario);
                        adminUsuarioNormal.getUsuarioNormal().get(i).setContrase単a(contra);
                    }
                }
                adminUsuarioNormal.escrbirArchivo();
                JOptionPane.showMessageDialog(null, "Se ha modificado el usuario correctamente. ");
            }

        } catch (Exception e) {

        }    }//GEN-LAST:event_bt_modificarInformacionMouseClicked

    private void bt_eliminarseSistemaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_eliminarseSistemaMouseClicked
        try {
            if (jt_informacionPersonalUsuarioComun.getSelectedRow() >= 0) {
                DefaultTableModel modelo = (DefaultTableModel) jt_informacionPersonalUsuarioComun.getModel();
                modelo.removeRow(jt_informacionPersonalUsuarioComun.getSelectedRow());
                jt_informacionPersonalUsuarioComun.setModel(modelo);
                adminUsuarioNormal.cargarArchivo();
                String nombreModificar = JOptionPane.showInputDialog(null, "Ingrese su nombre porfavor: ");
                for (int i = 0; i < adminUsuarioNormal.getUsuarioNormal().size(); i++) {
                    if (adminUsuarioNormal.getUsuarioNormal().get(i).getNombreCompleto().equals(nombreModificar)) {
                        adminUsuarioNormal.getUsuarioNormal().remove(i);
                    }
                }
                adminUsuarioNormal.escrbirArchivo();
                JOptionPane.showMessageDialog(null, "Se ha eliminado su usuario correctamente. ");
                jd_UsuarioNormal.dispose();
            }
        } catch (Exception e) {
        }    }//GEN-LAST:event_bt_eliminarseSistemaMouseClicked

